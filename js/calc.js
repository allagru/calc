function sum() {
    let x = prompt("Введите первое число", 5);
    let y = prompt("Введите второе число", 1);
    let result = +x + +y;
    alert(`Результат сложения: ${result}`);
}
function sub() {
    let x = prompt("Введите первое число", 5);
    let y = prompt("Введите второе число", 1);
    let result = x - y;
    alert(`Результат вычитания: ${result}`);
}
function div() {
    let x = prompt("Введите первое число", 5);
    let y = prompt("Введите второе число", 1);
    let result = x / y;
    alert(`Результат деления: ${result}`);
}
function mul() {
    let x = prompt("Введите первое число", 5);
    let y = prompt("Введите второе число", 1);
    let result = x * y;
    alert(`Результат умножения: ${result}`);
}